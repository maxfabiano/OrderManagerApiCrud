# Caminho para o certificado e senha
$CertPath = "D:\Old\ProjetosPessoais\OrderManagerCrud\certificado\certificado.pfx"
$CertPassword = "102065VS"
$TimeStampURL = "http://timestamp.digicert.com"

# Diretório base para os arquivos
$BaseDir = "D:\Old\ProjetosPessoais\OrderManagerCrud"

# Caminho completo do signtool
$SignToolPath = "C:\Program Files (x86)\Windows Kits\10\bin\10.0.26100.0\x64\signtool.exe"

# Verificar se o signtool existe
if (!(Test-Path -Path $SignToolPath)) {
    Write-Error "Signtool não encontrado no caminho especificado: $SignToolPath"
    Read-Host "Pressione qualquer tecla para sair..."
    exit 1
}

# Buscar todos os arquivos no diretório e subdiretórios
$Files = Get-ChildItem -Path $BaseDir -Recurse -File

# Assinar cada arquivo
foreach ($File in $Files) {
    try {
        Write-Host "Assinando arquivo: $($File.FullName)"
        & $SignToolPath sign /f $CertPath /p $CertPassword /fd SHA256 /t $TimeStampURL $File.FullName
        if ($LASTEXITCODE -ne 0) {
            throw "Erro ao assinar o arquivo: $($File.FullName). Código de saída: $LASTEXITCODE"
        }
    } catch {
        Write-Error "Erro durante a assinatura do arquivo: $($File.FullName)"
        Write-Error $_.Exception.Message
    }
}

Write-Host "Processo concluído."
Read-Host "Pressione qualquer tecla para finalizar..."

